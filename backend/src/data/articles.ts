/**
 * Get a random article that hasn't been used recently
 */
export const getRandomArticle = (excludeTitles: string[]): Article => {
  const available = ARTICLES.filter(article => !excludeTitles.includes(article.title));
  
  if (available.length === 0) {
    // If all articles have been used, reset and return a random one
    return ARTICLES[Math.floor(Math.random() * ARTICLES.length)];
  }
  
  return available[Math.floor(Math.random() * available.length)];
};
